class Employee {
  constructor(firstname, lastname, role, manager) {
    this.first_name = firstname;
    this.last_name = lastname;
    this.role_id = role;
    this.manager_id = manager;
  }

  getName() {
    return this.getName;
  }

  getId() {
    return this.getId
  }

  getRole() {
    return this.role;
  }

  getManager() {
    return this.manager;
  }
}

module.exports = Employee;
